import sys
input = sys.stdin.readline

n = int(input().rstrip())
dp = [0] * (n+1)

dp[0] = 1
dp[1] = 1


for i in range(2, n+1):
    dp[i] = dp[i-1] + 2 * dp[i-2]

# print(dp)
print(dp[n] % 10007)
